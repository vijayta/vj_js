
function SetInfo(){
  var user_email = document.getElementById('user_email').value;
  var user_url = document.getElementById('user_url').value;
  this.user_email_result = validateEmail(user_email);
  this.user_url_result = validateUrl(user_url);
  return this.user_email_result, this.user_url_result;
}

