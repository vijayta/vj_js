function Url(){
}

Url.prototype.getUrl = function(){
  this.webAdd = prompt("Enter URL: ");
}

Url.prototype.validateUrl = function(){
  result = (this.webAdd  != null && this.webAdd.trim() != "" && url_regex.test(this.webAdd))
  if(!result) {
    alert('Please enter a valid url');
  }
  return result;
}

Url.prototype.getAndValidateUrl = function(){
  do{
    this.getUrl();
  }while(!this.validateUrl());
}

Url.prototype.dispURL = function(){
  window.open(this.webAdd, "", "height=450 , width=400, scrollbars=no");
}

window.onload = function(){
  var u = new Url();
  u.getAndValidateUrl();
  u.dispURL();
}